package com.general.logics;

public class AdditionLogics {

	public int getAdditionOfTwoNumbers(int n1, int n2) {
		return n1 + n2;
	}

	public int getAdditionOfThreeNumbers(int n1, int n2, int n3) {
		return n1 + n2 + n3;
	}
	
	public int getBonusAddition(int n1, int n2){
		return n1 + n2 + 5;
	}
}
